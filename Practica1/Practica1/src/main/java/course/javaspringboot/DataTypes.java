package course.javaspringboot;

public class DataTypes {
        /*  Características de los tipos de datos
        Contenido Almacenado      Valor binario directo             Dirección de memoria (puntero al Heap)
        Ubicación Principal       Stack (Pila de ejecución)         Heap (Montón de memoria)
        Mecanismo de Parámetros   Paso-por-Valor (Copia del valor)  Paso-por-Valor (Copia de la referencia)
        Mutabilidad del Original  Inmutable por el metodo           Mutable si se accede a atributos
    */


    // Primitivos
    byte b;       // 8 bits
    short s;      // 16 bits
    int i;        // 32 bits
    long l;       // 64 bits
    float f;      // 32 bits
    double d;     // 64 bits
    char c;       // 16 bits
    boolean bool; // 1 bits

    // Referencias
    Byte by;
    Short sh;
    Integer num;
    Double dbl;
    Long lng;
    Float fl;
    Character ch;
    Boolean bo;
}
